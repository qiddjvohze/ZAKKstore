<?php 
	include 'includes/session.php';

	if(isset($_POST['id'])){
		$id = $_POST['id'];
		
		$conn = $pdo->open();

		$stmt = $conn->prepare("SELECT *, products1.id AS prodid, products1.name AS prodname, category1.name AS catname FROM products1 LEFT JOIN category1 ON category1.id=products1.category_id WHERE products1.id=:id");
		$stmt->execute(['id'=>$id]);
		$row = $stmt->fetch();
		
		$pdo->close();

		echo json_encode($row);
	}
?>
